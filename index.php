<z>The knapsack problem or rucksack problem is a problem in combinatorial optimization: Given a set of items, each with a mass and a value, determine the number of each item to include in a collection so that the total weight is less than or equal to a given limit and the total value is as large as possible. Several algorithms are available to solve knapsack problems, based on dynamic programming approach,branch and bound approach or hybridizations of both approaches.
</z><br><img src="images/knapsack_problem.png" align="right" width="480" height="300">
<a id="knapsack_problem"></a>
<b><u>Algorithm:</u></b><br>The <b>0-1 Knapsack Problem</b> <z>is the problem of choosing a subset of the n items such that the corresponding profit sum is maximized without having the weight sum to exceed the capacity c.
</z><br><b><u>Instructions to solve:</u></b><br>
<ul><z>
<li>1.Define the weight of the container.</li>
<li>2.Define the weight of the item.</li>
<li>3.Define the cost of the item.</li>
<li>4.The algorithm randomly determines the objects that best fit in the container, based on its weight.</li></z></ul>
