<z>Tabu search (TS) is a metaheuristic algorithm that can be used for solving combinatorial optimization problems (problems where an optimal ordering and selection of options is desired). Current applications of TS span the areas of resource planning, telecommunications,VLSI design, financial analysis, scheduling, space planning, energy distribution, molecular engineering, <br>logistics, pattern classification, flexible manufacturing, waste management, mineral exploration, biomedical analysis, environmental conservation and scores of others.
<br><br><img src="images/tabu search.PNG" align="right" width="480" height="350">

<br>The implementation of tabu search uses memory structures that describe the visited solutions or user-provided sets of rules.
One of the main components of Tabu Search is its use of adaptive memory, which creates a more flexible search behavior.
Memory-based strategies are therefore the hallmark of tabu search approaches, founded on a quest for “integrating principles, by which alternative forms of memory are appropriately combined with effective strategies for exploiting them. 
 <br><br>
<b><u>Basic Tabu Search Algorithm:</u></b>
<ul>
<li><b>Step 1:</b>Choose an initial solution i in S.  Set i* = i and k=0.</li>

<li><b>Step 2:</b> Set k=k+1 and generate a subset V* of solution in N(i,k) such that either one of the Tabu conditions is violated or at least one of the aspiration conditions holds.</li>

<li><b>Step 3:</b> Choose a best j in V* and set i=j.</li>

<li><b>Step 4:</b> If f(i) < f(i*) then set i* = i.<li>

<li><b>Step 5:</b> Update Tabu and aspiration conditions.</li>

<li><b>Step 6:</b> If a stopping condition is met then stop.  Else go to Step 2.</li>
</ul>
