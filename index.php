<z>In the bin packing problem, objects of different volumes must be packed into a finite number of bins or containers each of volume V in a way that minimizes the number of bins used. In computational complexity theory, it is a combinatorial NP-hard problem.
There are many variations of this problem, such as 2D packing, linear packing, packing by weight, packing by cost, and so on.</z>
<img src="images/binpack.gif" align="right" width="500" height="350">
<br><b><u>Algorithm:</u></b><br>
<b>The Bin-Packing Problem (BPP)</b> <z>can be described, using the terminology of Knapsack problems, as follows.<br> Given n items and n knapsacks (or bins), with <br>  wj = weight of item j. <br> c = capacity of each bin.<br>
Assign each item to one bin so that the total weight of the items in each bin does not exceed c and <br>the number of bins used is a minimum. </z>
<br>
<a id="linear_packing_problem"></a>
